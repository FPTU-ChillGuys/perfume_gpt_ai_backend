/**
 * Các hằng số TTL (Time-To-Live) dùng cho @Cacheable decorator.
 * Giá trị tính bằng giây (seconds).
 */

/** 1 giây */
export const CACHE_TTL_1SEC = 1 * 1000;

/** 5 giây */
export const CACHE_TTL_5SEC = 5 * 1000;

/** 10 giây */
export const CACHE_TTL_10SEC = 10 * 1000;

/** 15 giây */
export const CACHE_TTL_15SEC = 15 * 1000;

/** 20 giây */
export const CACHE_TTL_20SEC = 20 * 1000;

/** 30 giây */
export const CACHE_TTL_30SEC = 30 * 1000;

/** 1 phút */
export const CACHE_TTL_1MIN = 60 * 1000;

/** 5 phút */
export const CACHE_TTL_5MIN = 5 * 60 * 1000;

/** 15 phút */
export const CACHE_TTL_15MIN = 15 * 60 * 1000;

/** 30 phút */
export const CACHE_TTL_30MIN = 30 * 60 * 1000;

/** 1 giờ (mặc định) */
export const CACHE_TTL_1HOUR = 60 * 60 * 1000;

/** 2 giờ */
export const CACHE_TTL_2HOUR = 2 * 60 * 60 * 1000;

/** 6 giờ */
export const CACHE_TTL_6HOUR = 6 * 60 * 60 * 1000;

/** 12 giờ */
export const CACHE_TTL_12HOUR = 12 * 60 * 60 * 1000;

/** 24 giờ / 1 ngày */
export const CACHE_TTL_1DAY = 24 * 60 * 60 * 1000;

export const CACHE_TTL_1WEEK = 7 * 24 * 60 * 60 * 1000;

export const CACHE_TTL_1MONTH = 30 * 24 * 60 * 60 * 1000;

export const CACHE_TTL_1YEAR = 365 * 24 * 60 * 60 * 1000;
