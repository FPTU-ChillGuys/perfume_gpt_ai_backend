/** 15 phút */
export const CACHE_TTL_15MIN = 15 * 60;

/** 30 phút */
export const CACHE_TTL_30MIN = 30 * 60;

/** 1 giờ (mặc định) */
export const CACHE_TTL_1HOUR = 60 * 60;

/** 2 giờ */
export const CACHE_TTL_2HOUR = 2 * 60 * 60;

/** 6 giờ */
export const CACHE_TTL_6HOUR = 6 * 60 * 60;

/** 12 giờ */
export const CACHE_TTL_12HOUR = 12 * 60 * 60;

/** 24 giờ / 1 ngày */
export const CACHE_TTL_1DAY = 24 * 60 * 60;
